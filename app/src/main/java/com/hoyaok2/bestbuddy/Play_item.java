package com.hoyaok2.bestbuddy;

public class Play_item {

    int no;
    int pic;
    String title;
    String subtitle;
    int price;
    int phone;
    String file;
    int favor;

    public Play_item() {
    }

    public Play_item(int no, int pic, String title, String subtitle, int price, int phone, String file, int favor) {
        this.no = no;
        this.pic = pic;
        this.title = title;
        this.subtitle = subtitle;
        this.price = price;
        this.phone = phone;
        this.file = file;
        this.favor = favor;
    }
}
